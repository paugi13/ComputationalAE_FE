function [Ne, BeXi] = Hexaedra8N(xi,eta,zeta)

Ne = 1/8*[(1-xi)*(1-eta)*(1-zeta) (1+xi)*(1-eta)*(1-zeta) (1-xi)*...
    (1+eta)*(1-zeta) (1-xi)*(1+eta)*(1-zeta) (1-xi)*(1-eta)*(1+zeta) (1+xi)...
    *(1-eta)*(1+zeta) (1+xi)*(1+eta)*(1+zeta) (1-xi)*(1+eta)*(1+zeta)];

BeXi = 1/8*[-(1-eta)*(1-zeta) (1-eta)*(1-zeta) (1+eta)*(1-zeta) -(1+eta)*...
    (1-zeta) -(1-eta)*(1+zeta) (1-eta)*(1+zeta) (1+eta)*(1+zeta) -(1+eta)*...
    (1+zeta);
    -(1-xi)*(1-zeta) -(1+xi)*(1-zeta) (1+xi)*(1-zeta) (1-xi)*(1-zeta) -(1-xi)...
    *(1+zeta) -(1+xi)*(1+zeta) (1+xi)*(1+zeta) (1-xi)*(1+zeta);
    -(1-xi)*(1-eta) -(1+xi)*(1-eta) -(1+xi)*(1+eta) -(1-xi)*(1+eta) (1-xi)...
        *(1-eta) (1+xi)*(1-eta) (1+xi)*(1+eta) (1-xi)*(1+eta)];
end

